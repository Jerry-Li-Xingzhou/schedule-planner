function searchCitiesLocal(query) {
    if (!query) return [];
    const all = [...CITY_DB, ...DISTRICT_DB];
    const q = query.toLowerCase();
    return all
        .filter(c => c.name.includes(query) || c.region.includes(query) || c.name.toLowerCase().includes(q))
        .sort((a, b) => {
            const aStarts = a.name.startsWith(query) || a.region.startsWith(query) ? 1 : 0;
            const bStarts = b.name.startsWith(query) || b.region.startsWith(query) ? 1 : 0;
            return bStarts - aStarts;
        })
        .slice(0, 8);
}

async function searchCity(name, count = 5) {
    try {
        const url = `https://geocoding-api.open-meteo.com/v1/search?name=${encodeURIComponent(name)}&count=${count}&language=zh`;
        const res = await fetch(url);
        const data = await res.json();
        if (data.results && data.results.length > 0) {
            return data.results.sort((a, b) => (b.population || 0) - (a.population || 0)).map(r => {
                // Build full hierarchy: name > admin2 > admin1 > country
                const parts = [];
                if (r.name && r.name !== r.admin1) parts.push(r.name);
                if (r.admin2 && r.admin2 !== r.name) parts.push(r.admin2);
                if (r.admin1) parts.push(r.admin1);
                const country = r.country_code === 'CN' ? '' : (r.country || '');
                if (country) parts.push(country);
                return {
                    lat: r.latitude, lon: r.longitude,
                    name: parts[0] || r.name,
                    region: parts.slice(1).join('，') || '',
                };
            });
        }
    } catch (e) {}
    return [];
}

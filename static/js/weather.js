let weatherCache = {};
let weatherCacheTime = {};
let weatherFetchTime = null;

async function fetchWeather(lat, lon, force = false) {
    const key = `${lat.toFixed(2)},${lon.toFixed(2)}`;
    // Cache for 30 minutes unless forced
    if (!force && weatherCache[key] && Date.now() - (weatherCacheTime[key] || 0) < 1800000) {
        return weatherCache[key];
    }
    try {
        const url = `https://api.open-meteo.com/v1/forecast?latitude=${lat}&longitude=${lon}&daily=temperature_2m_max,temperature_2m_min,weathercode&hourly=temperature_2m,weathercode&timezone=auto&forecast_days=3`;
        const res = await fetch(url);
        const data = await res.json();
        weatherCache[key] = data;
        weatherCacheTime[key] = Date.now();
        weatherFetchTime = new Date();
        return data;
    } catch (e) {
        console.error('Weather fetch failed:', e);
        return null;
    }
}

function getWeatherFetchTime() {
    if (!weatherFetchTime) return '';
    return weatherFetchTime.toLocaleTimeString('zh-CN', { hour: '2-digit', minute: '2-digit' });
}

function getHourlyForDate(weatherData, dateStr) {
    if (!weatherData || !weatherData.hourly) return [];
    const hours = [];
    for (let i = 0; i < weatherData.hourly.time.length; i++) {
        const t = weatherData.hourly.time[i];
        if (t.startsWith(dateStr)) {
            const code = weatherData.hourly.weathercode[i];
            const w = WEATHER_CODES[code] || { icon: '🌡️', label: '未知' };
            hours.push({
                hour: parseInt(t.slice(11, 13)),
                icon: w.icon,
                temp: Math.round(weatherData.hourly.temperature_2m[i]),
                label: w.label,
            });
        }
    }
    return hours;
}

function getWeatherForDate(weatherData, dateStr) {
    if (!weatherData || !weatherData.daily) return null;
    const idx = weatherData.daily.time.indexOf(dateStr);
    if (idx < 0) return null;
    const code = weatherData.daily.weathercode[idx];
    const w = WEATHER_CODES[code] || { icon: '🌡️', label: '未知' };
    return {
        ...w,
        max: Math.round(weatherData.daily.temperature_2m_max[idx]),
        min: Math.round(weatherData.daily.temperature_2m_min[idx]),
    };
}

